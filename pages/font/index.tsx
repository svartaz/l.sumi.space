const convert = (font, value) => {
  let r = value.normalize('NFD')

  const letterss = lettersss[font]
  for (const k of Object.keys(lettersss.default))
    if (k in letterss) {
      const letters = letterss[k]
      for (let i = 0; i < lettersss.default[k].length; i++)
        r = r.replace(new RegExp(lettersss.default[k][i], 'g'), [...letters][i])
    }

  return r.normalize('NFC')
}

export default () => {
  const textDefault = '0123456789\nThe Quick Brown Fox Jumps over the Lazy Dog\nÎÎµÏƒÎºÎµÏ€Î¬Î¶Ï‰ Î¤Î·Î½ Î¨Ï…Ï‡Î¿Ï†Î¸ÏŒÏÎ± Î£Î±Ï‚ Î’Î´ÎµÎ»Ï…Î³Î¼Î¯Î±'
  const fontDefault = 'default'
  const [font, setFont] = useState(fontDefault);
  const [text, setText] = useState(textDefault);
  const [converted, setConverted] = useState(convert(fontDefault, textDefault));

  return <Page title='unicode font converter'>
    <div className={style.textareas}>
      <textarea value={text} onChange={event => { setText(event.target.value); setConverted(convert(font, event.target.value)) }}></textarea>
      <textarea value={converted} readOnly></textarea>
    </div>
    <select value={font} onChange={event => { setFont(event.target.value); setConverted(convert(event.target.value, text)) }}>{
      Object.keys(lettersss).map(k => <option key={k} value={k}>{
        k.replace(/[A-Z]/g, it => ' ' + it.toLowerCase())
      }</option>)
    }</select>
  </Page>
}